/**
 * Primed Clinic Survey Embed Loader
 *
 * Include this script on any host page to embed the survey form:
 *
 *   <div id="primed-survey"
 *     data-mode="signup"
 *     data-referral="ABC123"
 *     data-email="user@example.com"
 *     data-first-name="John"
 *     data-last-name="Doe"
 *     data-phone="+61412345678">
 *   </div>
 *   <script src="https://your-survey-host.com/embed.js"></script>
 *
 * For the treatment questionnaire flow, use:
 *   data-mode="questionnaire"
 *   data-treatment-name="hair-loss"
 *   data-treatment-id="5"
 */
(function () {
  var el = document.getElementById("primed-survey");
  if (!el) {
    console.error(
      '[Primed Survey] Mount element not found. Add <div id="primed-survey"> to your page.'
    );
    return;
  }

  // Read config from data attributes and expose to the React app
  window.SURVEY_CONFIG = {
    mode:          el.dataset.mode          || "signup",
    treatmentName: el.dataset.treatmentName || "",
    treatmentId:   el.dataset.treatmentId   || "",
    referralCode:  el.dataset.referral      || "",
    email:         el.dataset.email         || "",
    firstName:     el.dataset.firstName     || "",
    lastName:      el.dataset.lastName      || "",
    phone:         el.dataset.phone         || "",
  };

  // Derive the base URL from this script's own src
  var scriptEl = document.currentScript;
  if (!scriptEl) {
    console.error("[Primed Survey] Could not determine script base URL.");
    return;
  }
  var base = scriptEl.src.replace(/\/embed\.js(\?.*)?$/, "");

  // Fetch the asset manifest generated by the React build,
  // then inject the CSS and JS bundles into the host page
  fetch(base + "/asset-manifest.json")
    .then(function (r) {
      if (!r.ok) throw new Error("HTTP " + r.status);
      return r.json();
    })
    .then(function (manifest) {
      var entrypoints = manifest.entrypoints || [];

      var jsFiles = [];
      entrypoints.forEach(function (file) {
        // Strip leading "./" if present
        var filePath = file.replace(/^\.\//, "");

        if (filePath.endsWith(".css")) {
          var link = document.createElement("link");
          link.rel = "stylesheet";
          link.href = base + "/" + filePath;
          document.head.appendChild(link);
        } else if (filePath.endsWith(".js")) {
          jsFiles.push(filePath);
        }
      });
      // Load JS files sequentially via onload callbacks
      (function loadNext(i) {
        if (i >= jsFiles.length) return;
        var script = document.createElement("script");
        script.src = base + "/" + jsFiles[i];
        script.onload = function () { loadNext(i + 1); };
        document.head.appendChild(script);
      })(0);
    })
    .catch(function (err) {
      console.error("[Primed Survey] Failed to load asset manifest:", err);
    });
})();
